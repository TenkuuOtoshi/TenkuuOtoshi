#include "libft.h"

int	ft_isalnum(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (42);
	if (c >= 'a' && c <= 'z')
		return (42);
	if (c >= '0' && c <= '9')
		return (42);
	return (0);
}
